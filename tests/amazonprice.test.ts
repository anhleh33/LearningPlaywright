import { test } from '@playwright/test'
import nodemailer from 'nodemailer'
import * as auth from '../login.json'

const url = "https://www.amazon.com/s?k=kitchen+and+dining&crid=1CR304DY9B6JB&sprefix=%2Caps%2C327&ref=nb_sb_ss_recent_1_0_recent"

test("Amazon price drop notification", async ({ page }) => {
    await page.goto(url)
    await page.waitForLoadState('load')
    await page.waitForSelector('.a-price-whole')
    const prices = page.locator('.a-price-whole')
    
    let totalPrice: number = 0

    await Promise.all(
        (await prices.all()).map(async ele => {
            const priceText = await ele.innerText()
            const priceValue = Number(priceText.replace(/,/g, ''))
            if (!isNaN(priceValue)) {
                totalPrice += priceValue
            }
        })
    )
    console.log(totalPrice + "VND")

    // FIX 1: Add 'await' here so the test waits for the email to finish
    await sendEmailNotification(totalPrice, await prices.count())
})

